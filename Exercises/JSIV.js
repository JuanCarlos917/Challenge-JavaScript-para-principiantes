function isValid(s) {
  // Tu cÃ³digo acÃ¡:
  let stack = [];
  let mapping = {
      ')': '(',
      ']': '[',
      '}': '{'
  }

  for (let i = 0; i < s.length; i++) {
      let c = s.charAt(i);
      if (c === '(' || c === '[' || c === '{') {
          stack.push(c);
      } else if (stack.length === 0 || stack[stack.length - 1] !== mapping[c]) {
          return false;
      } else {
          stack.pop();
      }
  }
  return stack.length === 0;

}

